export class MockAgentWebSocket {
  private timers: ReturnType<typeof setTimeout>[] = [];
  private cancelled = false;

  constructor(
    query: string,
    private onMessage: MessageHandler,
    private onClose: CloseHandler
  ) {
    this.run(query);
  }

  private run(query: string) {
    const script = buildScript(query);
    let elapsed = 0;

    for (const entry of script) {
      elapsed += entry.delayMs;
      const t = setTimeout(() => {
        if (this.cancelled) return;
        this.onMessage({
          type: "log",
          payload: {
            id: generateId(),
            step: entry.step,
            message: entry.message,
            timestamp: new Date().toISOString(),
          },
        });
      }, elapsed);
      this.timers.push(t);
    }

    // Send the report after all logs
    const reportDelay = elapsed + 300;
    const reportTimer = setTimeout(() => {
      if (this.cancelled) return;
      this.onMessage({
        type: "report",
        payload: {
          id: generateId(),
          title: query,
          markdown: MOCK_REPORT_MARKDOWN(query),
          createdAt: new Date().toISOString(),
          wordCount: 380,
          sources: ["Gartner", "arXiv", "OpenAI", "Groq", "Tavily"],
        },
      });

      // Complete signal
      setTimeout(() => {
        if (!this.cancelled) {
          this.onMessage({ type: "complete", payload: { message: "done" } });
          this.onClose();
        }
      }, 300);
    }, reportDelay);
    this.timers.push(reportTimer);
  }

  disconnect() {
    this.cancelled = true;
    this.timers.forEach(clearTimeout);
    this.onClose();
  }
}