import type { WsMessage, LogStep } from "@/types";
import { generateId } from "@/lib/utils";

type MessageHandler = (msg: WsMessage) => void;
type CloseHandler = () => void;

interface MockLogScript {
  step: LogStep;
  message: string;
  delayMs: number;
}

function buildScript(query: string): MockLogScript[] {
  return [
    { step: "search",     message: `[🔍] Searching Tavily for "${query}"...`,             delayMs: 600  },
    { step: "fetch",      message: "[🌐] Fetching top 5 source documents...",              delayMs: 900  },
    { step: "analyze",    message: "[🧠] Extracting key facts and entities...",            delayMs: 1200 },
    { step: "reason",     message: "[💡] Running chain-of-thought reasoning pass 1/3...", delayMs: 800  },
    { step: "reason",     message: "[💡] Running chain-of-thought reasoning pass 2/3...", delayMs: 900  },
    { step: "reason",     message: "[💡] Running chain-of-thought reasoning pass 3/3...", delayMs: 700  },
    { step: "synthesize", message: "[📝] Synthesizing findings into structured report...", delayMs: 1400 },
    { step: "save",       message: "[💾] Saving report to artifacts store...",             delayMs: 600  },
    { step: "complete",   message: "[✅] Research complete. Report ready.",                delayMs: 400  },
  ];
}

